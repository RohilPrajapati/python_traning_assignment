# Write a program to demonstrate data types that can be elements of a tuple.

# sting tuple
thistuple = ('hello','this', 'is', 'tuple')
print(thistuple)

# number tuple
thisNumberTuple=(1,2,3,4,5)
print(thisNumberTuple)

# float tuple
thisFloatTuple = (1.4,1.8,4.55,5.77,2.44)
print(thisFloatTuple)

# this is tuple
thisIsTuple = ("hello",)
print(type(thisIsTuple))

# this is not tuple
thisIsNotTuple = ("hello")
print(type(thisIsNotTuple))

# boolean tuple
thisIsBoolTuple = (True,False,False,True)
print(thisIsBoolTuple)
